package com.sctjsj.mayk.wowallethost.model.javabean;

import java.util.List;

/**
 * Created by mayikang on 17/5/16.
 */

public class ProjectBean {
    private int id;

    //项目名称
    private String name;

    //所属分类
    private List<String> classifyList;

    //项目展示图
    private String icon;

    //项目图片列表
    private ProjectImageData imageData;

    //H5介绍
    private String introduce;

    public ProjectBean() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<String> getClassifyList() {
        return classifyList;
    }

    public void setClassifyList(List<String> classifyList) {
        this.classifyList = classifyList;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public ProjectImageData getImageData() {
        return imageData;
    }

    public void setImageData(ProjectImageData imageData) {
        this.imageData = imageData;
    }

    public String getIntroduce() {
        return introduce;
    }

    public void setIntroduce(String introduce) {
        this.introduce = introduce;
    }
}
